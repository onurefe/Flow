  uint16_t comp_x, comp_y;
  comp_x = (uint16_t)qfp_fmul(qfp_fadd(0.5f, qfp_fmul(vx, (1.0f / DEFAULT_PWM_RANGE))), qfp_int2float(TIM1_RUN_RELOAD));
